# Sum of List items
list1 = [10, 20, 30, 40, 50]
total = sum(list1)
print("The sum of the list items is:", total)

# Sum of items in multiple lists
list2 = [5, 15, 25]
total_sum = list1 + list2
total_sum = sum(total_sum)
print("The sum of the items in both lists is:", total_sum)

# Output:
# The sum of the list items is: 150
# The sum of the items in both lists is: 180