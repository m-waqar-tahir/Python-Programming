# Access characters by index
my_string = "Hello, Waqar Tahir!"
print("The first character is:", my_string[0])
print("The fifth character is:", my_string[4])

# Negative indexing
print("The last character is:", my_string[-1])
print("The second-to-last character is:", my_string[-2])

# Output:
# The first character is: H
# The fifth character is: o
# The last character is: !
# The second-to-last character is: r